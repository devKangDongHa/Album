package com.project.album.model;

public class PhotoVO {

	String photo_seq;
	String photo_name;
	String photo_day;
	String category_seq;
	String folder_seq;
	String photo_like;
	String orgfilename;
	String filesize;
	
	public String getPhoto_seq() {
		return photo_seq;
	}
	public void setPhoto_seq(String photo_seq) {
		this.photo_seq = photo_seq;
	}
	public String getPhoto_name() {
		return photo_name;
	}
	public void setPhoto_name(String photo_name) {
		this.photo_name = photo_name;
	}
	public String getPhoto_day() {
		return photo_day;
	}
	public void setPhoto_day(String photo_day) {
		this.photo_day = photo_day;
	}
	public String getCategory_seq() {
		return category_seq;
	}
	public void setCategory_seq(String category_seq) {
		this.category_seq = category_seq;
	}
	public String getFolder_seq() {
		return folder_seq;
	}
	public void setFolder_seq(String folder_seq) {
		this.folder_seq = folder_seq;
	}
	public String getPhoto_like() {
		return photo_like;
	}
	public void setPhoto_like(String photo_like) {
		this.photo_like = photo_like;
	}
	public String getOrgfilename() {
		return orgfilename;
	}
	public void setOrgfilename(String orgfilename) {
		this.orgfilename = orgfilename;
	}
	public String getFilesize() {
		return filesize;
	}
	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}
	
	
	
}
